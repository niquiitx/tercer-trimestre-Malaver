
def main():
    numero1 = 15
    numero2 = 4.5
    numero3 = 10

    suma = numero1 + numero2
    resta = numero1 - numero3
    multiplicacion = numero2 * numero3
    division = numero1 / numero3
    division_entera = numero1 // numero3
    modulo = numero1 % numero3
    potencia = numero3 ** 2

    print("=== CALCULADORA BÁSICA ===")
    print(f"Suma: {suma}")
